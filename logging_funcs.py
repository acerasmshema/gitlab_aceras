import datetime

# =============================================================================
# Functions
# =============================================================================
def log_message(logfile_path = 'log.txt', message='error'):
    with open(logfile_path,'a+') as f:
        f.write('{} - {}\n'.format(datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S"),message))
